export const MEI_CHARTER_GLOSS_KO =
  "한 극(死·四·仇·丑·低·魔)이 없으면 다른 극(生·八·情·美·高·佛)의 이름도 성립하기 어렵다는 상대성의 시(没…哪有…). 중생은 한쪽 끝에 매달리고(众生执一极), 성인은 중도를 걷는다(圣人执中道). 극을 붙잡지 않으면 죽음·삶·미움·정·낮음·높음·나쁨·좋음·마(魔)·불(佛)의 꼬리표도 함께 느슨해진다(没死没生 … 没魔没佛). 끝은 没·未·无로 수렴한다. 금강경의 상(相)을 타파하는 지혜를 현대어로 옮긴다면, ‘고정된 이미지에 매몰되지 않는다’에 가깝다."

export const LUMINA_MEI_COMPLIANCE_PRINCIPLE_KO =
  "没·中道: 극단적 미학·가격·효능 이분법으로 소비자를 압박하지 말고 **검증 가능한 본질 서술**로 균형 잡을 것. **제품 범주·성능을 부정하는 역설 카피**(예: 화장품이 아니다 등 허위·오인)는 금지."

export function isMeiCharterEnabled(): boolean {
  return process.env.LUMINA_MEI_CHARTER?.trim() !== "0"
}

